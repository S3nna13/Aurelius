"""AlpacaEval-style pairwise evaluation for AureliusTransformer.

Compares two model outputs (model vs. reference) using an LLM judge
and computes win rate. Pure Python — no external dependencies.
"""

from __future__ import annotations

import logging
import random
import re
from collections.abc import Callable
from dataclasses import dataclass

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Config & result dataclasses
# ---------------------------------------------------------------------------


@dataclass
class PairwiseConfig:
    """Configuration for pairwise evaluation."""

    n_annotators: int = 1
    temperature: float = 0.0
    reference_model: str = "reference"
    annotator_model: str = "judge"


@dataclass
class PairwiseResult:
    """Result of a single pairwise comparison."""

    instruction: str
    output_a: str  # model being evaluated
    output_b: str  # reference
    winner: str  # "A", "B", or "tie"
    judge_reasoning: str = ""
    confidence: float = 1.0


# ---------------------------------------------------------------------------
# Prompt building & parsing
# ---------------------------------------------------------------------------


def build_pairwise_prompt(instruction: str, output_a: str, output_b: str) -> str:
    """Build a prompt for an LLM judge to compare two responses.

    Args:
        instruction: The original user instruction / question.
        output_a: The response from the model being evaluated.
        output_b: The reference response.

    Returns:
        A formatted prompt string.
    """
    return (
        f"You are an impartial judge evaluating two responses to the following instruction.\n\n"
        f"Instruction:\n{instruction}\n\n"
        f"Response A:\n{output_a}\n\n"
        f"Response B:\n{output_b}\n\n"
        f"Compare the two responses above based on helpfulness, accuracy, and quality.\n"
        f"Decide which response is better overall.\n"
        f"You MUST end your evaluation with exactly one of the following lines:\n"
        f"Winner: A\n"
        f"Winner: B\n"
        f"Winner: tie\n"
    )


def extract_pairwise_winner(judge_output: str) -> str:
    """Parse the winner from judge output.

    Looks for "Winner: A", "Winner: B", or "Winner: tie" (case-insensitive).
    Defaults to "tie" if the pattern is not found.

    Args:
        judge_output: Raw text generated by the judge model.

    Returns:
        One of "A", "B", or "tie".
    """
    match = re.search(r"winner\s*:\s*(A|B|tie)\b", judge_output, re.IGNORECASE)
    if match:
        raw = match.group(1).strip()
        if raw.upper() == "A":
            return "A"
        if raw.upper() == "B":
            return "B"
        return "tie"
    return "tie"


# ---------------------------------------------------------------------------
# Annotator
# ---------------------------------------------------------------------------


class PairwiseAnnotator:
    """Annotates instruction/response pairs using an LLM judge.

    Args:
        generate_fn: Callable that takes a prompt string and returns a
            response string.  All model interaction goes through this.
        config: PairwiseConfig controlling annotation behaviour.
    """

    def __init__(self, generate_fn: Callable[[str], str], config: PairwiseConfig):
        self.generate_fn = generate_fn
        self.config = config

    def annotate(
        self,
        instruction: str,
        output_a: str,
        output_b: str,
    ) -> PairwiseResult:
        """Annotate a single pair.

        When n_annotators > 1, runs the judge that many times and takes the
        majority vote.  Confidence is the fraction of annotators that agreed
        with the majority.

        Args:
            instruction: The original user instruction.
            output_a: Model response being evaluated.
            output_b: Reference response.

        Returns:
            A PairwiseResult with the determined winner.
        """
        prompt = build_pairwise_prompt(instruction, output_a, output_b)
        n = self.config.n_annotators

        votes: list[str] = []
        last_reasoning = ""
        for _ in range(n):
            judge_output = self.generate_fn(prompt)
            last_reasoning = judge_output
            votes.append(extract_pairwise_winner(judge_output))

        # Majority vote
        counts: dict[str, int] = {"A": 0, "B": 0, "tie": 0}
        for v in votes:
            counts[v] += 1

        winner = max(counts, key=lambda k: counts[k])
        # Break ties between "A" and "B" by preferring "tie"
        max_count = counts[winner]
        winners_with_max = [k for k, v in counts.items() if v == max_count]
        if len(winners_with_max) > 1:
            # Multiple outcomes tied for most votes — default to "tie"
            winner = "tie"

        confidence = counts[winner] / n if n > 0 else 1.0

        return PairwiseResult(
            instruction=instruction,
            output_a=output_a,
            output_b=output_b,
            winner=winner,
            judge_reasoning=last_reasoning,
            confidence=confidence,
        )

    def annotate_batch(
        self,
        items: list[tuple[str, str, str]],
    ) -> list[PairwiseResult]:
        """Annotate a batch of (instruction, output_a, output_b) tuples.

        Args:
            items: List of (instruction, output_a, output_b) tuples.

        Returns:
            List of PairwiseResult in the same order as the input.
        """
        return [self.annotate(instr, a, b) for instr, a, b in items]


# ---------------------------------------------------------------------------
# Win-rate computation
# ---------------------------------------------------------------------------


def compute_win_rate(results: list[PairwiseResult]) -> dict[str, float]:
    """Compute aggregate win/loss/tie rates from a list of results.

    Args:
        results: List of PairwiseResult objects.

    Returns:
        Dict with keys: win_rate, loss_rate, tie_rate, n_comparisons.
        All rates are 0.0 when results is empty.
    """
    n = len(results)
    if n == 0:
        return {
            "win_rate": 0.0,
            "loss_rate": 0.0,
            "tie_rate": 0.0,
            "n_comparisons": 0,
        }

    wins = sum(1 for r in results if r.winner == "A")
    losses = sum(1 for r in results if r.winner == "B")
    ties = sum(1 for r in results if r.winner == "tie")

    return {
        "win_rate": wins / n,
        "loss_rate": losses / n,
        "tie_rate": ties / n,
        "n_comparisons": n,
    }


# ---------------------------------------------------------------------------
# End-to-end pipeline
# ---------------------------------------------------------------------------


class AlpacaEvalPipeline:
    """End-to-end AlpacaEval-style evaluation pipeline.

    Args:
        annotator: A PairwiseAnnotator instance.
    """

    def __init__(self, annotator: PairwiseAnnotator):
        self.annotator = annotator

    def evaluate(
        self,
        instructions: list[str],
        model_outputs: list[str],
        reference_outputs: list[str],
    ) -> dict[str, object]:
        """Evaluate all instruction/output pairs.

        Args:
            instructions: List of user instructions.
            model_outputs: Corresponding outputs from the model being evaluated.
            reference_outputs: Corresponding reference outputs.

        Returns:
            Dict containing win_rate, loss_rate, tie_rate, n_comparisons,
            and a 'results' key with the per-item PairwiseResult list.
        """
        items = list(zip(instructions, model_outputs, reference_outputs))
        results = self.annotator.annotate_batch(items)
        metrics = compute_win_rate(results)
        metrics["results"] = results  # type: ignore[assignment]
        return metrics

    def bootstrap_confidence_interval(
        self,
        results: list[PairwiseResult],
        n_bootstrap: int = 100,
        ci: float = 0.95,
    ) -> tuple[float, float]:
        """Compute a bootstrap confidence interval for win_rate.

        Args:
            results: List of PairwiseResult objects.
            n_bootstrap: Number of bootstrap resamples.
            ci: Confidence level (e.g. 0.95 for 95% CI).

        Returns:
            (lower, upper) bounds of the confidence interval.
        """
        if not results:
            return (0.0, 0.0)

        rng = random.Random(42)
        n = len(results)
        bootstrap_rates: list[float] = []

        for _ in range(n_bootstrap):
            sample = [rng.choice(results) for _ in range(n)]
            rate = compute_win_rate(sample)["win_rate"]
            bootstrap_rates.append(rate)

        bootstrap_rates.sort()
        alpha = 1.0 - ci
        lower_idx = int((alpha / 2) * n_bootstrap)
        upper_idx = int((1.0 - alpha / 2) * n_bootstrap) - 1
        upper_idx = min(upper_idx, n_bootstrap - 1)
        lower_idx = max(lower_idx, 0)

        return (bootstrap_rates[lower_idx], bootstrap_rates[upper_idx])
